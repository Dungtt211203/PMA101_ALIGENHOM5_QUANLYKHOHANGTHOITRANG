    public ChatLieu GetDataFromGui() {
        String id = this.lblId.getText();
        String ma = this.txtMa.getText().trim();
        String ten = this.txtTen.getText().trim();
        if (ma.equals("")) {
            JOptionPane.showMessageDialog(this, "Mã không được để trống");
            return null;
        }
        if (ten.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên không được để trống");
            return null;
        }
        ChatLieu c = new ChatLieu(id, ma, ten);
        return c;
    }
    
    private void LoadTable() {
        _DefaultTableModel = (DefaultTableModel) tblchatlieu.getModel();
        _DefaultTableModel.setRowCount(0);
        for (ChatLieuViewModel x : _Service.GetAll()) {
            _DefaultTableModel.addRow(new Object[]{
                x.getId(),
                x.getMa(),
                x.getTen(),});
        }
    }
    
    private boolean checkMaTrung(String ma) {
        try {
            Connection conn = DBContext.getConnection();
            PreparedStatement ps = conn.prepareStatement("SELECT COUNT(*) FROM chatlieu WHERE Ma = ?");
            ps.setString(1, ma);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                int count = rs.getInt(1);
                if (count > 0) {
                    return true;
                }
            }
        } catch (Exception e) {
            System.out.println("Lỗi kết nối tại checkMaTrung()");
            e.printStackTrace();
        }
        return false;
    }
    
    boolean testdata() {
        if (txtMa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã chất liệu không dược để trống");
            return false;
        }
        if (txtTen.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên chất liệu không được để trống");
            return false;
        }      
        return true;
    }

