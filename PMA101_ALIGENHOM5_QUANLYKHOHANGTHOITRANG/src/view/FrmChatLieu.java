    private void tblchatlieuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblchatlieuMouseClicked
        // TODO add your handling code here:
        int index = tblchatlieu.getSelectedRow();
        if (index == -1) {
            return;
        }
        _IdWhenClick = (String) tblchatlieu.getModel().getValueAt(index, 0);
        String ma = (String) tblchatlieu.getValueAt(index, 1);
        String ten = (String) tblchatlieu.getValueAt(index, 2);
        
        lblId.setText(_IdWhenClick);
        txtMa.setText(ma);
        txtTen.setText(ten);
    }//GEN-LAST:event_tblchatlieuMouseClicked

    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        
        String ma = this.txtMa.getText().trim();
        if (this.testdata()) {
            if (checkMaTrung(ma)) {
                JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
                return;
            } else {
                ChatLieu temp = GetDataFromGui();
                temp.setId(_IdWhenClick);
                int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm chất liệu này?", "Xác nhận thêm", JOptionPane.YES_NO_OPTION);
                if (choice == JOptionPane.YES_OPTION) {
                    JOptionPane.showMessageDialog(this, _Service.themChatLieu(temp));
                    _IdWhenClick = "";
                    LoadTable();
                    Clear();
                }
            }
        }
    }//GEN-LAST:event_btnthemActionPerformed
    

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        
        int selectedRow = tblchatlieu.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng trong bảng để sửa");
            return;
        }
        if (this.testdata()) {
            ChatLieu temp = GetDataFromGui();
            temp.setId(_IdWhenClick);
            int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa chất liệu này?", "Xác nhận sửa", JOptionPane.YES_NO_OPTION);
            if (choice == JOptionPane.YES_OPTION) {
                JOptionPane.showMessageDialog(this, _Service.suaChatLieu(temp));
                _IdWhenClick = " ";
                LoadTable();
                Clear();
            }
        }

    }//GEN-LAST:event_btnsuaActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        int selectedRow = tblchatlieu.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng trong bảng để xóa");
            return;
        } else {
            ChatLieu temp = GetDataFromGui();
            temp.setId(_IdWhenClick);
            int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa chất liệu này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
            if (choice == JOptionPane.YES_OPTION) {
                JOptionPane.showMessageDialog(this, _Service.xoaChatLieu(temp));
                _IdWhenClick = " ";
                LoadTable();
                Clear();
            }
        }
    }//GEN-LAST:event_btnxoaActionPerformed

