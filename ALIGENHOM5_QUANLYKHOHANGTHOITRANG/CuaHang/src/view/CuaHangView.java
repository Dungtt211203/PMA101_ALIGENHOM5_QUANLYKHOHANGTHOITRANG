    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (this.testdata()) {
            if (ss.insertCH(this.readForm()) > 0) {
                JOptionPane.showMessageDialog(this, "Them thanh cong");
                this.fillTable(ss.getAll());
            } else {
                JOptionPane.showMessageDialog(this, "them that bai");
            }
        }

    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (this.testdata()) {
            index = tblBang.getSelectedRow();
            if (index == -1) {
                JOptionPane.showMessageDialog(this, "Bạn chưa chọn sinh viên cần sửa.");
            } else {
                String ma = tblBang.getValueAt(index, 1).toString();
                CuaHang ch = readForm(); // Get the updated SinhVien object from the form
                if (ss.update(ma, ch) > 0) {
                    JOptionPane.showMessageDialog(this, "Sửa thành công.");
                    this.fillTable(ss.getAll());
                } else {
                    JOptionPane.showMessageDialog(this, "Sửa thất bại.");
                }
            }
        }


    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        index = tblBang.getSelectedRow();
        if (index == -1)
            JOptionPane.showMessageDialog(this, "ban chua chon dong de xoa");
        else {
            String ma = tblBang.getValueAt(index, 1).toString();
            if (ss.deleteCH(ma) > 0) {
                JOptionPane.showMessageDialog(this, "xoa thanh cong");
                this.fillTable(ss.getAll());
            } else {
                JOptionPane.showMessageDialog(this, "xoa that bai");
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    CuaHang readForm() {
        String ma, ten, dc, tp, qg;
        ma = txtMa.getText().trim();
        ten = txtTen.getText().trim();
        dc = txtDc.getText().trim();
        tp = txtTp.getText().trim();
        qg = txtQg.getText().trim();
        return new CuaHang(null, ma, ten, dc, tp, qg);
    }

    boolean testdata() {
        if (txtMa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "mã cửa hàng không dược để trống");
            return false;
        }
        if (txtTen.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "tên cửa hàng không được để trống");
            return false;
        }
        if (txtDc.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Địa chỉ cửa hàng không được để trống");
            return false;
        }
        if (txtTp.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Thành phố  không được để trống");
            return false;
        }
        if (txtQg.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Quốc Gia không được để trống");
            return false;
        }
        return true;
    }

