    void fillTable(List<CuaHang> list) {
        model = (DefaultTableModel) tblBang.getModel();
        //xóa sạch bảng cũ
        model.setRowCount(0);
        for (CuaHang x : list) {
            model.addRow(x.toDataRow());
        }
    }

